$(document).ready(function() {
        $("a[name='min']").click(function(){
        	$("#sidebar").hide();
        	$(".col-sm-9").removeClass().addClass("col-sm-12");
        	$(".col-md-10").removeClass().addClass("col-md-12");
        	$(".col-md-offset-2").removeClass();
        	$(".col-sm-offset-3").removeClass();
        });
        $("a[name='max']").click(function(){
        	$("#sidebar").show();
        	$(".col-sm-12").removeClass().addClass("col-sm-9").addClass("col-sm-offset-3");
        	$(".col-md-12").removeClass().addClass("col-md-10").addClass("col-md-offset-2");
        });
    });